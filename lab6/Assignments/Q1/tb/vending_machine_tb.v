module vending_machine_tb;

    reg clk;
    reg rst;
    reg I;
    reg J;

    wire X;
    wire Y;

    vending_machine DUT(
        .clk(clk),
        .rst(rst),
        .I(I),
        .J(J),
        .X(X),
        .Y(Y)
    );

    //--------------------------------------------------
    // Clock Generation
    //--------------------------------------------------
    initial
        clk = 0;

    always #5 clk = ~clk;

    //--------------------------------------------------
    // Tasks
    //--------------------------------------------------

    task no_coin;
    begin
        I = 0;
        J = 0;
        #10;
    end
    endtask

    task one_rupee;
    begin
        I = 1;
        J = 0;
        #10;

        I = 0;
        J = 0;
        #10;
    end
    endtask

    task two_rupee;
    begin
        I = 1;
        J = 1;
        #10;

        I = 0;
        J = 0;
        #10;
    end
    endtask

    //--------------------------------------------------
    // Stimulus
    //--------------------------------------------------
    initial
    begin

        rst = 1;
        I   = 0;
        J   = 0;

        #15;
        rst = 0;

        //--------------------------------------------------
        // Case 1 : 1 + 1 + 1 = 3
        //--------------------------------------------------
        one_rupee;
        one_rupee;
        one_rupee;

        #20;

        //--------------------------------------------------
        // Case 2 : 1 + 2 = 3
        //--------------------------------------------------
        one_rupee;
        two_rupee;

        #20;

        //--------------------------------------------------
        // Case 3 : 2 + 1 = 3
        //--------------------------------------------------
        two_rupee;
        one_rupee;

        #20;

        //--------------------------------------------------
        // Case 4 : 2 + 2 = 4
        //--------------------------------------------------
        two_rupee;
        two_rupee;

        #40;

        $finish;
    end

    //--------------------------------------------------
    // Monitor
    //--------------------------------------------------
    initial
    begin
        $monitor(
        "Time=%0t I=%b J=%b X=%b Y=%b",
        $time,I,J,X,Y);
    end

endmodule